def build_session_context(client_id: str | None, channel: str | None) -> str:
    """Строка контекста сессии, добавляемая в начало диалога."""
    authorized_channels = {"chat_intern", "mobile"}
    is_auth = bool(client_id) and channel in authorized_channels
    lines = ["# Контекст текущей сессии"]
    lines.append(f"Канал: {channel or 'неизвестен'}")
    if is_auth:
        lines.append(f"Клиент авторизован. client_id = {client_id}.")
        lines.append(
            "Можно вызывать клиентские инструменты с этим client_id и обсуждать его собственные "
            "договоры и заявки."
        )
    else:
        lines.append("Клиент НЕ авторизован (анонимный канал).")
        lines.append(
            "Отвечай только на общие информационные вопросы. НЕ вызывай клиентские инструменты "
            "и не раскрывай персональные данные."
        )
    return "\n".join(lines)
